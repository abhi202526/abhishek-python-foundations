# print(): is a built-in function- a ready-made tool Python ships with. Its whole job: take whatever you give it, and display it as text on your screen.

print("Hello") # quotes (" ") tell Python "treat this as literal text (a string)."
print(42)
print(3.14)
print(True)

print("Age:", 20)

# Comments - notes for humans, invisible to Python

# This entire line is ignored by Python
print("This runs") # this part after the hash is also ignored
